function Timeline() {
  const [collapsedPeriods, setCollapsedPeriods] = useState({});
  const [expandedEvents, setExpandedEvents] = useState({});

  const togglePeriod = (periodId) => {
    setCollapsedPeriods(prev => ({
      ...prev,
      [periodId]: !prev[periodId]
    }));
  };

  const toggleEvent = (eventKey) => {
    setExpandedEvents(prev => ({
      ...prev,
      [eventKey]: !prev[eventKey]
    }));
  };

  return (
    <main className="timeline-container">
      <div className="container">
        <header className="timeline-header">
          <h2>中国近代史时间轴</h2>
          <p>从鸦片战争到新中国成立（1840-1949）</p>
        </header>

        {timelineData.map((period) => (
          <section key={period.id} id={period.id} className={`timeline-period ${collapsedPeriods[period.id] ? 'collapsed' : ''}`}>
            <div className="period-header" onClick={() => togglePeriod(period.id)}>
              <h3 className="period-title">
                <span className="period-years">{period.years}</span>
                <span className="period-name">{period.name}</span>
              </h3>
              <button className="toggle-btn" aria-label="展开/折叠">
                <span className="toggle-icon">{collapsedPeriods[period.id] ? '▶' : '▼'}</span>
              </button>
            </div>
            <div className="period-content">
              <div className="events-container">
                {period.events.map((event, idx) => {
                  const eventKey = `${period.id}-${idx}`;
                  return (
                    <div
                      key={eventKey}
                      className={`event-item ${event.treaty ? 'treaty-event' : ''} ${expandedEvents[eventKey] ? 'expanded' : ''}`}
                      onClick={() => toggleEvent(eventKey)}
                    >
                      <div className="event-time">{event.time}</div>
                      <div className="event-content">
                        <div className="event-title">{event.title}</div>
                        <div className="event-detail">{event.detail}</div>
                        <div className="event-detail-expand">
                          <p dangerouslySetInnerHTML={{ __html: event.expand }} />
                        </div>
                      </div>
                    </div>
                  );
                })}
              </div>
            </div>
          </section>
        ))}
      </div>
    </main>
  );
}

export default function Home() {
  const {siteConfig} = useDocusaurusContext();
  return (
    <Layout
      title="中国近代史时间轴"
      description="从鸦片战争到新中国成立（1840-1949）">
      <BrowserOnly>
        {() => <Timeline />}
      </BrowserOnly>
    </Layout>
  );
}
